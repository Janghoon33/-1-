
public class ex02 {

	public static void main(String[] args) {
	 
		// 변수 선언하기 -> int num = 3;
		// 상수 선언하기 -> final int num = 3;
		// 상수는 다른 데이터값으로 수정이 불가능 하다!
		
		final int num = 3; 
		
		System.out.println(num);
		
	    System.out.println(num);
        

//		 class 파일 실행 단축키 : ctrl + f11
		

	}

}
