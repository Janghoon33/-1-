
public class Ex11_switch_case {

	public static void main(String[] args) {
		
		int num = 40;
		
		switch (num) {
		case 10:
			System.out.println("10이 들어있습니다!");
			break;
		case 20:
			System.out.println("20이 들어있습니다!");
			break;
		case 30:
			System.out.println("30이 들어있습니다!");
			break;
			
		default	:
			System.out.println("10,20,30이 아닙니다");
			

		
		}
		}
		
		

	}


